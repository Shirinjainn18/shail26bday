const lettersPopup = document.getElementById("lettersPopup");
const closeLetterBtn = document.getElementById("closeLetterBtn");
const closeLetterBg = document.getElementById("closeLetterBg");

const popupLetterImg = document.getElementById("popupLetterImg");
const popupLetterText = document.getElementById("popupLetterText");
const popupLetterName = document.getElementById("popupLetterName");

const passwordProtectedLetters = new Set([
    "rishabh",
    "suyash",
    "tanmay",
    "rishi",
    "prasidh",
    "shirin",
    "namrashi"
]);

function openLetter(selectedLetter) {
    popupLetterImg.src = selectedLetter.image;
    popupLetterImg.alt = selectedLetter.name;
    popupLetterText.innerHTML = selectedLetter.text;
    popupLetterName.textContent = selectedLetter.name;

    lettersPopup.classList.add("active");
    document.body.style.overflow = "hidden";
}

letterCards.forEach((card) => {
    card.addEventListener("click", function (e) {
        e.preventDefault();

        const letterId = this.dataset.letterId;
        const selectedLetter = letters[letterId];

        if (!selectedLetter) return;

        if (passwordProtectedLetters.has(letterId)) {
            const enteredPassword = prompt("Enter password to open this letter:");

            if (enteredPassword !== selectedLetter.password) {
                alert("Wrong password. Please try again.");
                return;
            }
        }

        openLetter(selectedLetter);
    });
});

function closeLetter() {
    lettersPopup.classList.remove("active");
    document.body.style.overflow = "";
}

closeLetterBtn.addEventListener("click", closeLetter);
closeLetterBg.addEventListener("click", closeLetter);

document.addEventListener("keydown", function (e) {
    if (e.key === "Escape") {
        closeLetter();
    }
});
